public class Fractions {

   public static void main (String [] args){
   
      
      for(int x = 2; x < 26; x++){
         
         System.out.print("1 / " + x + " = ");
         System.out.printf("%.5f", (1.0 / x)); 
         System.out.println();
         
           
      }//end loop
   

   
   }//end main



}//end class Fractions